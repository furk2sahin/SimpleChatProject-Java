    public void showMails(){
        ArrayList<Email> emails = databaseOperations.getEmails(this.user.getId(), this.userTo.getId());
        for(Email email : emails){
            Object[] addingObjects = {String.valueOf(email.getId()), email.getContent()};
            model.addRow(addingObjects);
        }
    }

