    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
       int selectedRow = mailTables.getSelectedRow();
       warning.setText("");
       if(selectedRow == -1){
           warning.setForeground(Color.red);
           warning.setText("Please select a row first.");
       }else {
           databaseOperations.deleteEmail(Integer.valueOf(mailTables.getValueAt(selectedRow, 0).toString()));
           model.removeRow(selectedRow);
           contentArea.setText("");
           warning.setForeground(Color.green);
           warning.setText("Successfully deleted.");
       }
    }//GEN-LAST:event_deleteButtonActionPerformed

