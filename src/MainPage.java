    private void oldMailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oldMailsButtonActionPerformed
        warning.setForeground(Color.red);
        warning.setText("");
        String comboboxItem = comboboxField.getSelectedItem().toString();
        User userTo = databaseOperations.getUser(comboboxItem);
        ArrayList<Email> emails = databaseOperations.getEmails(this.user.getId(), userTo.getId());
        if(emails.size() > 0){
            OldMessages oldMails = new OldMessages();
            oldMails.setUser(this.user, userTo);
            oldMails.setVisible(true);
        }else
            warning.setText("There is no old mails");
        
    }//GEN-LAST:event_oldMailsButtonActionPerformed

