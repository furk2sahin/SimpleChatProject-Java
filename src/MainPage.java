    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        String content = mailContent.getText();
        if(content.trim().length() == 0){
            warning.setText("Please write a content");
        }else{
            String comboboxItem = comboboxField.getSelectedItem().toString();
            User userTo = databaseOperations.getUser(comboboxItem);
            if(databaseOperations.addEmail(this.user.getId(), userTo.getId(), content)){
                warning.setForeground(Color.yellow);
                warning.setText("Mail sended.");
            }else{
                warning.setForeground(Color.red);
                warning.setText("You have submitted that content before.");
            }
        }  
        mailContent.setText("");
    }//GEN-LAST:event_sendButtonActionPerformed

